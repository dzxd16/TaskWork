package homework1;

public class MathMetods {
    public static float Add(float par1, float par2){
        return par1+par2;
    }
    public static float Decrease(float par1, float par2){
        return par1-par2;
    }
    public static float Multy(float par1, float par2){
        return par1*par2;
    }
    public static float Divide(float par1, float par2){
           return par1/par2;
    }

}
